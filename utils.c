#include "proto.h"

void	ft_putstr(char *s)
{
	int	i;

	i = -1;
	while (s[++i])
		write(1, s[i], 1);
}

int	tablen(int *s)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}
